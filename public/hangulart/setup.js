function getline(sx, sy, wsize, lineno) {
	stroke(0);
	strokeWeight(1);

	push()
	translate(sx, sy);
	for (let i = 0; i < lineno; i++) {
		let x = i * wsize / lineno;
		line(x, 0, x, wsize)
		line(0, x, wsize, x)
	}
	noFill()
	rect(0, 0, wsize, wsize);
	pop()
}


